import type { PolledMarket } from "../schemas/polled-market.js";
import { domainOf, isEmptySource, isGate1Match } from "./buckets.js";
import { KIND_LABELS, categorize, isDeterministicFeed, type MarketKind } from "./categorize.js";

export interface SamplePick {
  bucket: string;
  rule: string;
  market: PolledMarket | null;
}

const SAMPLE_RULE =
  "truly IO-shaped (drops chainlink, empty source, deterministic feeds); 5 picks maximizing diversity over (kind, domain)";

function isEligibleForSample(m: PolledMarket): boolean {
  if (isGate1Match(m.eventResolutionSource)) return false;
  if (isEmptySource(m.eventResolutionSource)) return false;
  if (isDeterministicFeed(categorize(m))) return false;
  return true;
}

export function pickSamples(rows: PolledMarket[]): SamplePick[] {
  const eligible = rows.filter(isEligibleForSample).sort((a, b) => a.id.localeCompare(b.id));

  const picks: SamplePick[] = [];
  const usedKinds = new Set<MarketKind>();
  const usedDomains = new Set<string>();
  const usedIds = new Set<string>();

  while (picks.length < 5) {
    let best: PolledMarket | null = null;
    let bestScore = -1;

    for (const m of eligible) {
      if (usedIds.has(m.id)) continue;
      const k = categorize(m);
      const d = domainOf(m.eventResolutionSource);
      const score = (usedKinds.has(k) ? 0 : 2) + (usedDomains.has(d) ? 0 : 1);
      if (score > bestScore) {
        bestScore = score;
        best = m;
        if (score === 3) break;
      }
    }

    if (!best) break;

    const k = categorize(best);
    const d = domainOf(best.eventResolutionSource);
    usedKinds.add(k);
    usedDomains.add(d);
    usedIds.add(best.id);

    picks.push({
      bucket: `${KIND_LABELS[k]} · ${d}`,
      rule: SAMPLE_RULE,
      market: best,
    });
  }

  while (picks.length < 5) {
    picks.push({ bucket: "(no eligible market)", rule: SAMPLE_RULE, market: null });
  }

  return picks;
}

export function renderSampleMarkdown(date: string, picks: SamplePick[]): string {
  const lines: string[] = [];
  lines.push(`# pm-bench sample header — ${date}`);
  lines.push("");
  lines.push(
    `Five deliberately-different example markets — picks are truly IO-shaped (chainlink-fed, empty-source, and deterministic-feed markets are excluded) and maximize diversity over (kind, domain). Deterministic given the input JSONL.`,
  );
  lines.push("");
  for (let i = 0; i < picks.length; i++) {
    const p = picks[i]!;
    lines.push(`## ${i + 1}. ${p.bucket}`);
    lines.push("");
    lines.push(`Rule: _${p.rule}_`);
    lines.push("");
    if (!p.market) {
      lines.push(`> Bucket empty in this snapshot. No row picked.`);
      lines.push("");
      continue;
    }
    const m = p.market;
    lines.push(`- **id**: \`${m.id}\``);
    lines.push(`- **question**: ${m.question}`);
    lines.push(`- **endDate**: ${m.endDate}`);
    lines.push(`- **eventTitle**: ${m.eventTitle ?? "(none)"}`);
    lines.push(
      `- **eventResolutionSource**: ${m.eventResolutionSource ? `\`${m.eventResolutionSource}\`` : "(empty)"}`,
    );
    lines.push(
      `- **tags**: ${m.tags && m.tags.length > 0 ? m.tags.map((t) => `\`${t}\``).join(", ") : "(none)"}`,
    );
    lines.push(`- **outcomes**: ${m.outcomes.join(" / ")}`);
    lines.push(
      `- **outcomePrices**: ${m.outcomePrices ? m.outcomePrices.join(" / ") : "(none)"}`,
    );
    lines.push("");
  }
  lines.push(`---`);
  lines.push(`Generated by \`@gym/pm-bench\` sample.`);
  lines.push("");
  return lines.join("\n");
}
